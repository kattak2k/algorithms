# Fastest solution
def coin_change(coins, amount)
  dp = Array.new(amount + 1, -1)
  dp[0] = 0

  coins.each do |c|
    puts "dp: #{dp} , coin: #{c}"
    1.upto(amount) do |i|
      next if i < c || dp[i - c] == -1

      dp[i] = dp[i - c] + 1 if dp[i] == -1 || dp[i - c] + 1 < dp[i]

      puts "\ti: #{i}, dp: #{dp}, dp[i - c]: #{dp[i - c]} , dp[i]: #{dp[i]}"
    end
  end

  dp[amount]
end

